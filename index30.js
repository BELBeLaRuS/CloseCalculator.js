function openMenu() {
    let menu = document.getElementById("menu");
    if(menu){      
            menu.classList.add('opened');
    }
}

function hideMenu() {
    let menu = document.getElementById("menu");
    if(menu){
        menu.classList.remove('opened');
    }
}


